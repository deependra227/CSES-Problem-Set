bool compareBysecond(pair<lli, lli> a, pair<lli, lli> b)
{
	if (a.second == b.second) return a.first < b.first;

	return a.second < b.second;
}
int log(lli x)
{
	int cnt = 0;

	while (x > 0)
	{
		cnt++;
		x = x >> 1;
	}

	return cnt;
}
lli max(lli a, lli b)
{
	if (a < b) return b;
	else return a;
}
lli min(lli a, lli b)
{
	if (a > b) return b;
	else return a;
}
lli t[5001][5001];
lli solve(vector<int>& x, int l , int r)
{
	if(l == r) return x[l];

	if(t[l][r] != -1)return t[l][r];

	return t[l][r] = max(-solve(x, l + 1, r) + x[l], -solve(x, l, r - 1) + x[r]);
}

